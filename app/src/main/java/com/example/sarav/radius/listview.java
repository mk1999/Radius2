package com.example.sarav.radius;

public class listview {
    String pic,name,age;

    public listview(String pic, String name, String age) {
        this.pic = pic;
        this.name = name;
        this.age = age;
    }

    public String getPic() {
        return pic;
    }

    public String getName() {
        return name;
    }

    public String getAge() {
        return age;
    }

}
